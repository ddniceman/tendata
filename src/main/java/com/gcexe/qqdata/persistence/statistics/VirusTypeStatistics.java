package com.gcexe.qqdata.persistence.statistics;

public class VirusTypeStatistics {

	private int cnt;
	private int mcnt;
	private String virusType;

	public int getCnt() {
		return cnt;
	}

	public void setCnt(int cnt) {
		this.cnt = cnt;
	}

	public int getMcnt() {
		return mcnt;
	}

	public void setMcnt(int mcnt) {
		this.mcnt = mcnt;
	}

	public String getVirusType() {
		return virusType;
	}

	public void setVirusType(String virusType) {
		this.virusType = virusType;
	}

}
